<p><span style="font-size:16px; color:#820000;"><b>教師專業能力測驗中心簡介</b></span></p>

</div>
<font face="DFKai-sb">茲為配合<font color="#005CAF">教育部</font>推動「國民小學教師學科知能評量」方案，於一百零四年起受教育部委託國立臺中教育大學辦理師資生「國民小學國語、數學、社會、自然與生活科技領域」學科知能標準化成就評量工具建置計畫，國立臺中教育大學成立<font color="#005CAF">「教師專業能力測驗中心」</font>，專門開發及辦理各學科之<font color="#005CAF">教師專業能力測驗評量</font>。 </font><br>

<div style='text-align:center'>

<p><span style="font-size:16px; color:#820000;"><b>研發辦理理念</b></span></p>

</div>
<font face="DFKai-sb">一位具有高度專業能力的教師，除了具備正確教學信念外，也應具備基本的學科知能以及學科教學知識。2011年經濟合作暨開發組織(OECD)與美國聯邦教育部聯合召開之「國際教學專業高峰會議」，以「建構高素質的教學專業」為主題；2013年美國師資培育大學協會(AACTE)亦宣布啟動專門針對師資生準備度研發的全國性標準本位評量—教師表現評量(edTPA)，幫助評定師資生是否具備教師應有的職能。由此，不難發現專業標準本位之國際趨勢。<br>
為了因應<font color="#005CAF">「專業標準本位」</font>之國際趨勢，「師資培育及藝術教育司」不僅在「師資培育白皮書」中，提出建構「專業標準本位」及「師資培用理念」兼具師資培育體系之目標，並委請國立臺中教育大學教師專業能力測驗中心<font color="#005CAF">比照國際上著名大型測驗</font>，開發<font color="#005CAF">國民小學學科知能評量</font>。希冀藉由此工具幫助明瞭國民小學教師學科知能程度，進而瞭解國民小學教師專業知能不足之處並據以改善。</font><br>

<div style='text-align:center'>
<p><span style="font-size:16px; color:#820000;"><b>設置目標</b></span></p>
</div>
<font face="DFKai-sb">教師專業能力測驗中心（以下簡稱本中心）成立之目標在於提高教師資質與教學能力，加強全國師資任用與進修，並強化<font color="#005CAF">學科知能（CK）</font>、<font color="#005CAF">一般教學知能（PK）</font>與<font color="#005CAF">學科教學知能（PCK）</font>以精進師資生教學力及提升教學品質。
</font><br>
